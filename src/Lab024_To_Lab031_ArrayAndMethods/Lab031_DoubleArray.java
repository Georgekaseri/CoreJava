package CoreJava.src.Lab024_To_Lab031_ArrayAndMethods;

public class Lab031_DoubleArray {
    public static void main(String[] args) {
        /*
        Java program to illustrate creating a double arrays,
        puts some values in the array, and prints each value to standard output.
         */

        int [] arr1 = {95, 100, 250, 75, 185};
        for (int i = 0; i < arr1.length; i++) {
            System.out.println("This is double value of Arrays " + arr1[i]*2);

        }







    }
}
